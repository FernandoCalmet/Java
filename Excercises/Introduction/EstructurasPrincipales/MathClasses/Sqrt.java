public class Sqrt {
  public static void main(String[] args) {
    int numero = 8;
    /*
     * Math.sqrt(numero) : Raiz cuadrada de un numero.
     */
    int resultado = (int) Math.sqrt(numero);
    System.out.println("La raiz cuadrada de " + numero + " es " + resultado);
  }
}
