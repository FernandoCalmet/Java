/*
    Math.round(numero) : Redondeo de un numero hacia el natural mas cercano
*/
package ClaseMath;

public class Round {
    
    public static void main(String[] args) {
        
        double numero = 7.60;
        
        double redondeo = Math.round(numero);
        
        System.out.println(redondeo);
    }
    
}