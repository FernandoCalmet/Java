/*
    Math.ceil(numero) : Redondeo de un numero hacia el natural mas adelante
 */
package ClaseMath;

public class Ceil {
    
    public static void main(String[] args) {
        
        double numero = 7.60;
        
        double redondeo = Math.ceil(numero);
        
        System.out.println(redondeo);
        
    }
    
}
