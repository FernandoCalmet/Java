/*
    Math.floor(numero) : Redondeo de un numero hacia el natural mas atras
 */
package ClaseMath;

public class Floor {
    
    public static void main(String[] args) {
        
        double numero = 7.60;
        
        double redondeo = Math.floor(numero);
        
        System.out.println(redondeo);
        
    }
    
}
