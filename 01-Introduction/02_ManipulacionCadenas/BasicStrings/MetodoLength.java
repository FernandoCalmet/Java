package BasicStrings;

public class MetodoLength {
    public static void main(String[] args) {
        /*
         * cadenaTexto.lenght() : El metodo length() devuelve la longitud de una
         * variable tipo String.
         */
        String texto = "Cual es la dimension de esta cadena?";
        System.out.println("Su dimension es " + texto.length() + " de longitud.");
    }
}
